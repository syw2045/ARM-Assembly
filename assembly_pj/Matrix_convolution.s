ADD_result
	MOV r2,r6,LSR #31;Sign bit
	MOV r3,r12,LSR #31;Sign bit
	
	MOV r4,r6,LSL #1
	MOV r4,r4,LSR #24 ;Exponent
	MOV r5,r12,LSL #1
	MOV r5,r5,LSR #24 ;Exponent
	
	MOV r7,r6,LSL #9; Mantissa 
	MOV r7,r7,LSR #9
	MOV r8,r12,LSL #9; Mantissa
	MOV r8,r8,LSR #9
	
	ADD r7,r7,#0x800000 ;ADD 1 to mantisa
	ADD r8,r8,#0x800000
	
	CMP r4, r5
	SUBGT r9, r4, r5
	SUBLT r9, r5, r4  ;shift num
	
	CMP r4,r5
	MOVGT r8,r8,LSR r9 ;if shift num is not 0
	MOVLT r7,r7,LSR r9
	
	CMP r4,r5 ;compare exponent
	MOVGT r10,r4
	MOVLE r10,r5
	
	CMP r2,r3
	BEQ ADD_Equal
	B ADD_NotEqual

ADD_Equal	
	ADD r7,r7,r8 ;adding mantissa
	LDR r4, mantissa
	ADD r4, r4, r4
	CMP r7,r4
	MOVGT r7,r7,LSR #1
	ADDGT r10,r10,#1
	B Finish
	
ADD_NotEqual	
	CMP r7,r8 ;mantisa
	SUBGT r7,r7,r8
	SUBLT r7,r8,r7
	
	MOV r6,r6,lsl #1
	MOV r12,r12,lsl #1
	
	CMP r6,r12 ; declare sign
	MOVGT r2,r2 
	MOVLT r2,r3
	
	MOV r9,#0

Loop ; normalize
	MOV r11, r4
	CMP r11,r7
	MOVGT r7,r7,lsl #1
	ADDGT r9,r9,#1
	BGT Loop
	
	SUBNE r10,r10,r9 ;exponent
	
Finish
	MOV r4, #0x800000
	SUB r7,r7,r4
	
	MOV r2,r2,lsl #31 ; make sign bit
	MOV r10,r10,lsl #23 ; make exponent
	
	ADD r11,r2,r10 ; Exponent + mantissa
	ADD r11,r11,r7 ; result = S + Exponent + mantissa 
	
