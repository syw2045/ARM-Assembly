	SUB r6, r6, #127 ; -127 decide to exponent
	

	MOV r8, r3, LSL #9
	MOV r8, r8, LSR #9	;Mantissa
	MOV r9, r2, LSL #9
	MOV r9, r9, LSR #9	;Mantissa
	ADD r8, r8, #0x800000 ; add 1
	ADD r9, r9, #0x800000 ; add 1 

	

	MOV r8, r8, LSL #6 ; A 
	ADD r5, r8, r8 ; 2A
	MOV r9, r9, LSR #15 ; add 0 and LSR 16 bit ;
	MOV r10, #6 ;
	
Booth
	SUBS r10, r10, #1
	BEQ Normalize
	
	MOV r11, r9, LSL #29
	MOVS r11, r11, LSR #29
	BEQ SHIFT
	
	CMP r11, #2
	BLE ADD1
	
	CMP r11, #4
	BLT ADD2
	BEQ SUB2
	
	CMP r11, #6
	BLE SUB1
	BGT SHIFT

SHIFT
	MOV r9, r9, LSR #2 ; shift right
	MOV r7, r7, ASR #2 ; arithmathic shift
	B Booth

ADD1
	ADD r7, r7, r8
	MOV r7, r7, ASR #2; arithmathic shift
	MOV r9, r9, LSR #2; shift right
	B Booth

ADD2
	ADD r7, r7, r5
	MOV r7, r7, ASR #2; arithmathic shift
	MOV r9, r9, LSR #2; shift right
	B Booth

SUB1
	SUB r7, r7, r8
	MOV r7, r7, ASR #2; arithmathic shift
	MOV r9, r9, LSR #2; shift right
	B Booth
	
SUB2
	SUB r7, r7, r5
	MOV r7, r7, ASR #2; arithmathic shift
	MOV r9, r9, LSR #2; shift right
	B Booth

;===========
