MUL_Result
	MOV r4, r4, LSL #31 ; sign bit
	MOV r6, r6, LSL #23 ; exponent
	MOV r7, r7, LSL #6
	MOV r7, r7, LSR #9 ; mentissa
	ADDEQ r7, r7, #1
	ADD r6, r4, r6
	ADD r6, r6, r7
	CMP r12, #0
	MOVEQ pc, lr
	LDR r12, [r12]
	
	
;value1 : r6
;value2 : r12
;===========================================
;	 	 			sign 	exponent		mentissa
